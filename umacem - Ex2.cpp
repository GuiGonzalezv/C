#include <stdio.h>
#include <stdlib.h>

main(){
	
	int n, i, num;
	
	
	printf("FOR ==================================== \n");
	
	for(i=1; i <= 100; i++){
		printf("%d /", i);
	}
	
	printf("==================================================== \n");
	
	
	n = 1;
	printf("While ==================================== \n");
	
	while(n <= 100){
		printf("%d /", n);
		n = n+1;
	}
	
	printf("==================================================== \n");
	
	
	printf("Do while ==================================== \n");
	num = 1;
	
	do{
		printf("%d /", num);
		num = num + 1;
	}while(num <= 100);
	
	printf("===================================================== \n");
}
